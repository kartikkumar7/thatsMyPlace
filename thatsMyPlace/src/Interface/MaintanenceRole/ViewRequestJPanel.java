    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        rightPanel.remove(this);
        CardLayout layout = (CardLayout) rightPanel.getLayout();
        Component[] comps = this.rightPanel.getComponents();
        for (Component comp : comps) {
            if (comp instanceof MaintanenceHomePageJPanel) {
                MaintanenceHomePageJPanel panel = (MaintanenceHomePageJPanel) comp;
                panel.populate();
            }
        }
        layout.previous(rightPanel);
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String newStatus = cbStatus.getSelectedItem().toString();
        if (newStatus.equals("Solved")) {
            workRequest.setStatus(WorkRequest.Status.Solved);
            btnUpdate.setEnabled(true);
            btnSave.setEnabled(false);
            cbStatus.setEnabled(false);
        }
        else{
            JOptionPane.showMessageDialog(null, "Only can set status to solved", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed


