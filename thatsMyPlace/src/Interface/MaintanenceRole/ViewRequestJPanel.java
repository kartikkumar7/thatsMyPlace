    private void populate() {
        int id = workRequest.getId();
        UserAccount sender = workRequest.getSender();
        String notes = workRequest.getMessage();
        Date date = workRequest.getRequestDate();
        String status = workRequest.getStatus().toString();
       
        txtCustomerNotes.setText(notes);
        txtRequestDate.setText(dateFormat.format(date));
        txtRequestID.setText(Integer.toString(id));
        txtSender.setText(sender.getEmployee().getName());
                
        cbStatus.addItem("Pending");
        cbStatus.addItem("Solved");
        
        if(status.equals("Pending")) cbStatus.setSelectedIndex(0);
        else if(status.equals("Solved")) cbStatus.setSelectedIndex(1);
        else{
            System.out.println("no status");
        }
    }

