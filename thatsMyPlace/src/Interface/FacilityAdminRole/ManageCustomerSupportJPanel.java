    public void populate() {
        DefaultTableModel dtm = (DefaultTableModel) customerSupportJTable.getModel();
        dtm.setRowCount(0);
        
        ArrayList<UserAccount> userAccountList = business.getUserAccountDirectory().getUserAccounts();
        for (UserAccount ua: userAccountList) {
            if(ua.getRole().getName().equals("Customer Support")){
                Object[] row = new Object[dtm.getColumnCount()];
            
                row[0] = ua.getEmployee().getId();
                row[1] = ua.getEmployee();
                row[2] = ua;
                

                dtm.addRow(row);
            }
        }
    }
    
