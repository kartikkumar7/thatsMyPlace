    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String name = nameTextField.getText();
        String id = idTextField.getText();
        
         
        nameTextField.setBorder(new LineBorder(new Color(45,118,232)));
        idTextField.setBorder(new LineBorder(new Color(45,118,232)));
        
        lblEmployeeID.setForeground(new Color(45,118,232));
        lblEmployeeName.setForeground(new Color(45,118,232));
        
        // If no input, return
        if (name.equals("") && id.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter something", "Warning", JOptionPane.WARNING_MESSAGE);
            nameTextField.setBorder(BorderFactory.createLineBorder(Color.RED));
            idTextField.setBorder(BorderFactory.createLineBorder(Color.RED));
            populate();
            return;
        }
        
     
        int idInt = -1;
        if(id.length() != 0){
            try{
                idInt = Integer.parseInt(id);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, "Please enter Integer !", "Warning", JOptionPane.WARNING_MESSAGE);
                idTextField.setBorder(BorderFactory.createLineBorder(Color.RED));
                return;
            }
        }
        
        
        
        ArrayList<UserAccount> userAccountList = business.getUserAccountDirectory().getUserAccounts();
        ArrayList<UserAccount> newUserAccountList = new ArrayList<>();
        
        for (UserAccount ua: userAccountList) {
            boolean idBool = true;
            boolean nameBool = true;
            
            if(idInt != -1){
                idBool = (idInt == ua.getEmployee().getId() ? true : false);
            }
            if(name.length() != 0){
                nameBool = (name.equals(ua.getEmployee().getName()) ? true : false);
            }
            
            if( idBool  && nameBool){
                newUserAccountList.add(ua);
            }
            
        }
        
        if (newUserAccountList.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No Result!", "Warning", JOptionPane.WARNING_MESSAGE);
            idTextField.setText("");
            nameTextField.setText("");
            populate();
        }
        else {
            populate(newUserAccountList);
        }
        
        newUserAccountList.clear();
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        int selectedRow = customerSupportJTable.getSelectedRow();
        if (selectedRow >= 0) {
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete?", "Warning", selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                Employee employee = (Employee) customerSupportJTable.getValueAt(selectedRow, 1);
                UserAccount userAccount = (UserAccount) customerSupportJTable.getValueAt(selectedRow, 2);
                business.getEmployeeDirectory().getEmployeeList().remove(employee);
                business.getUserAccountDirectory().getUserAccounts().remove(userAccount);
                populate();
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select any row", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

