    private void populate() {
        FacilityEnterprise facilityEnterprise = null;
        for (Network network: business.getNetworkList()) {
            for (Enterprise e: network.getEnterpriseDirectory().getEnterpriseList()) {
                if (e.getEnterpriseType().equals(Enterprise.EnterpriseType.Facility)) {
                    facilityEnterprise = (FacilityEnterprise) e;
                    break;
                }
            }
            if (facilityEnterprise != null) break;
        }
        
        if (facilityEnterprise != null) {
            ArrayList<BasketballCourtAvailability> availabilityList = (ArrayList) facilityEnterprise.getBasketballCourtAvailDirectory().getList();
            for (BasketballCourtAvailability availability: availabilityList) {
                availableDates.add(availability.getDate());
            }
            ArrayList<LocalDate> localDateList = new ArrayList<>(availableDates);
            Collections.sort(localDateList);
            for (LocalDate localDate: localDateList) {
                cbNumberOfPeople.addItem(localDate.toString());
            }
        }
        else {
            System.out.println("Error!!!");
        }
    }
    
