    private void btnMakeBookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMakeBookingActionPerformed
        String selected = cbNumberOfPeople.getSelectedItem().toString();
        LocalDate selectedDate = null;
        for (LocalDate localDate: availableDates) {
            if (localDate.toString().equals(selected)) {
                selectedDate = localDate;
                break;
            }
        }
        CardLayout layout = (CardLayout) rightPanel.getLayout();
        rightPanel.add("StudentBookingJPanel", new StudentBookingJPanel(
                rightPanel, 
                userAccount, 
                enterprise, 
                business, 
                selectedDate));
        layout.next(rightPanel);
    }//GEN-LAST:event_btnMakeBookingActionPerformed

