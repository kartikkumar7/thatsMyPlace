    public void populate(){
        List<Booking> list = newFacility.getMasterBookingList().getMasterBookingList();
        DefaultTableModel dtm = (DefaultTableModel)bookingList.getModel();
        dtm.setRowCount(0);

        for(Booking b : list){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = b.getRegistrant().getName();
            row[1] = b.getDate();
            row[2] = b;
            row[3] = b.getEndTime();
            row[4] = b.getStatus();
            dtm.addRow(row); 
        }
    }

