    private void btnManageBookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnManageBookingActionPerformed
        // TODO add your handling code here:
   
        
        int selectedRow = bookingList.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row ","Information", JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            Booking bo = (Booking) bookingList.getValueAt(selectedRow, 2);
            
            if(bo.getStatus().equals(Booking.Status.Canceled)){
                JOptionPane.showMessageDialog(null, "Booking is Canceled! No action can be performed!");
                return;
            }
            else{
            ViewBookingJPanel panel = new ViewBookingJPanel(rightPanel, enterprise, bo);
            rightPanel.add("ViewBookingJPanel", panel);
            CardLayout layout = (CardLayout) rightPanel.getLayout();
            layout.next(rightPanel);
        
            }
        }
    }//GEN-LAST:event_btnManageBookingActionPerformed

