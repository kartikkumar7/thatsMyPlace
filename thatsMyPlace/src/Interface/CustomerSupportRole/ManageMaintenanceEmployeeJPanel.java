    public void populate() {
        this.allUserAccountList = business.getUserAccountDirectory().getUserAccounts();
        this.userAccountList.clear();
        for(UserAccount u : allUserAccountList){
            if(u.getRole().getName().equals("Maintanence"))
                userAccountList.add(u);
        }
        
        DefaultTableModel dtm = (DefaultTableModel) MaintenanceEmployeeList.getModel();
        dtm.setRowCount(0);
        
        for (UserAccount u : userAccountList) {
            Object[] row = new Object[dtm.getColumnCount()];
            
            row[0] = u.getEmployee().getId();
            row[1] = u.getEmployee().getName();
            row[2] = u;
            row[3] = 0;

            dtm.addRow(row);
        }
    }
    
    public void populate(ArrayList<UserAccount> list) {
        
        DefaultTableModel dtm = (DefaultTableModel) MaintenanceEmployeeList.getModel();
        dtm.setRowCount(0);
        
        for (UserAccount u : list) {
            Object[] row = new Object[dtm.getColumnCount()];
            
            row[0] = u.getEmployee().getId();
            row[1] = u.getEmployee().getName();
            row[2] = u;
            row[3] = u.getWorkQueue().getWorkRequestList().size();

            dtm.addRow(row);
        }
    }
    
    
    
