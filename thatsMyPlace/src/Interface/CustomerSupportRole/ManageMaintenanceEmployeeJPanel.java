    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String name = txtEmployeeName.getText();
        String id = txtEmployeeID.getText();
        
        txtEmployeeName.setBorder(new LineBorder(new Color(128, 128, 128)));
        txtEmployeeID.setBorder(new LineBorder(new Color(128, 128, 128)));
        
        lblEmployeeID.setForeground(Color.BLACK);
        lblEmployeeName.setForeground(Color.BLACK);
        
        // If no input, return
        if (name.equals("") && id.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter something", "Warning", JOptionPane.WARNING_MESSAGE);
            txtEmployeeName.setBorder(BorderFactory.createLineBorder(Color.RED));
            txtEmployeeID.setBorder(BorderFactory.createLineBorder(Color.RED));
            populate();
            return;
        }
        
        
        
        searchList = new ArrayList<>();
        boolean nameBool = true;
        boolean IdBool = true;
        for(UserAccount user : userAccountList){
            if(name.length() != 0){
                nameBool = name.equals(user.getEmployee().getName());
            }
            if(id.length() != 0){
                int intID = Integer.parseInt(id);
                IdBool = ( intID == user.getEmployee().getId());
            }
            
            if(  IdBool && nameBool) {              
                searchList.add(user);
            }
        }
       
        if (searchList.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No Result!", "Warning", JOptionPane.WARNING_MESSAGE);
            txtEmployeeID.setText("");
            txtEmployeeName.setText("");
            populate();
            
        }else{
            populate(searchList);
        }
        
        
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int selectedRow = MaintenanceEmployeeList.getSelectedRow();
        if (selectedRow >= 0) {
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete?", "Warning", selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                UserAccount useraccout = (UserAccount) MaintenanceEmployeeList.getValueAt(selectedRow, 2);
                business.getUserAccountDirectory().getUserAccounts().remove(useraccout);
                
                populate();
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select any row", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = MaintenanceEmployeeList.getSelectedRow();
        if (selectedRow >= 0) {
            UserAccount userAccount = (UserAccount) MaintenanceEmployeeList.getValueAt(selectedRow, 2);
            UpdateMaintenanceEmployeeJPanel panel = new UpdateMaintenanceEmployeeJPanel(rightPanel, userAccount, business);
            rightPanel.add("AddMaintenanceEmployeeJPanel", panel);
            CardLayout layout = (CardLayout) rightPanel.getLayout();
            layout.next(rightPanel);
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select any row", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

